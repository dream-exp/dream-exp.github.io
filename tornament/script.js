$('div.first-left').click(function() {
	if($('div.first-left').css("borderColor") != "rgb(255, 0, 0)") {
		$('div.first-left').css({
			'borderColor':'red'
		});
		$('div.third-right').css({
			'borderRight':'solid 5px red'
		});
	} else {
		$('div.first-left').css({
			'borderColor':'black'
		});
		$('div.third-right').css({
			'borderRight':'solid 5px black'
		});
	}
});

